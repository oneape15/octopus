package com.oneape.octopus.datasource;

/**
 * 数据类型
 */
public enum DataType {
    // 字符类型
    VARCHAR,
    // 二进制
    BINARY,
    // 布尔类型
    BOOLEAN,
    // 整型
    INTEGER,
    // 高精度
    DECIMAL,
    FLOAT,
    DOUBLE,
    // 日期
    DATE,
    // 时期
    TIME,
    // 时间戳
    TIMESTAMP,
    // 日期时间
    DATETIME;
}
